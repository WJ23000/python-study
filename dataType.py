#!/usr/bin/env python3
# -*- coding: utf-8 -*-

'''
python数据类型：
版本2：
    不可变：Number，Sring，Tuple(元素)
    可变：List，Dictionary(字典)，Set(集合)
版本3：Int，Float，Bool，Complex(复数)
'''


'''
python数据类型表示：
Tuple元祖用()表示，元祖可以包含List，List可以被改变
List用[]表示
String用''表示
Number用数字表示
Bool用True/False表示
Float用浮点数表示
Dictionary用{}表示，key：value方式存储数据
'''


'''
常用函数：
range(101)：生成一个小于101的整数列并转换成List
abs(-20)：求绝对值(正数的绝对值是它本身，负数的绝对值是正数)
max(1,2,4,8)：求最大值
min(2,4,8,12)：求最小值
isinstance(x, (int, float))：数据类型检查，对参数进行过滤
在Python中定义函数，可以用必选参数、默认参数、可变参数、关键字参数和命名关键字参数，这5种参数都可以组合使用。但是请注意，参数定义的顺序必须是：必选参数、默认参数、可变参数、命名关键字参数和关键字参数。
'''


'''
类型转换：
int(12.34)：转换成整数
float('12.34'): 转换成浮点数
str(12.34)：转换成字符串
hex(255)：把一个整数转换成十六进制表示的字符串
'''


'''
异常处理：
pass 可用于任何输出结果位置占位防止代码报错
'''